function ABCD = CascadeABCD(A1,A2)
    ABCD = zeros(size(A1));
    ABCD(1,1,:) = A1(1,1,:).*A2(1,1,:) + A1(1,2,:).*A2(2,1,:);
    ABCD(1,2,:) = A1(1,1,:).*A2(1,2,:) + A1(1,2,:).*A2(2,2,:);
    ABCD(2,1,:) = A2(1,1,:).*A1(2,1,:) + A1(2,2,:).*A2(2,1,:);
    ABCD(2,2,:) = A1(2,1,:).*A2(1,2,:) + A1(2,2,:).*A2(2,2,:);
end